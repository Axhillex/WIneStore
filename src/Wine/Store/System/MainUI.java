    private void jButtonOk1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOk1ActionPerformed
        // TODO add your handling code here:
        try{
            String updatedWName = jTextField8.getText();
            int updatedVintage = Integer.parseInt(jTextField12.getText());
            String updatedBrand = jTextField9.getText();
            String updatedCountry = jTextField10.getText();
            String updatedMNum = jTextField11.getText();
            float updatedAlch = Float.parseFloat(jTextField13.getText());
            int updatedPrice = Integer.parseInt(jTextField15.getText());
            if(updatedPrice == 0 || updatedWName.equals("") || !checkUpdatedVintage() || updatedBrand.equals("") || updatedCountry.equals("") || updatedMNum.equals("") || updatedAlch == 0.0f || checkUpdatedRadioButton() || checkUpdatedComboBox()){
                JOptionPane.showMessageDialog(jFrame3, "Incomplete Input!!", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                String wType = UpdatedRadioButVal;
                float btlSize = Float.parseFloat(UpdatedCheckBoxVal);

                JOptionPane.showMessageDialog(jFrame3, "Updated Successfully!!", "Update", JOptionPane.INFORMATION_MESSAGE);

                Object updatedData[] = {updatedWName, updatedVintage, updatedBrand, updatedCountry, updatedMNum, btlSize,wType, updatedAlch, updatedPrice};

                updateData(updatedData,manageRow);

                closeUpdatePanel();
            }
        }catch(NullPointerException | ClassCastException | NumberFormatException e){
            JOptionPane.showMessageDialog(jFrame3, "Invalid Input ", "Invalid Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOk1ActionPerformed

