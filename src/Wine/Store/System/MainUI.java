    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
        try{
            if(evt.getKeyCode() == KeyEvent.VK_ENTER){
                LinkedList<Integer> list = new LinkedList<>();
                int row = jTable1.getRowCount();
                for(int i = 0; i<row; i++){
                    list.add(Integer.parseInt((String) jTable1.getValueAt(i, 8)));
                }
                int x = Integer.parseInt(jTextField1.getText());
                new MergeSort().mergeSorting(list);
                int value = new BinarySearch().binarySearch(list, 1, list.size(), x);
                if(value == -1){
                    JOptionPane.showMessageDialog(this, "No Value Found", "Error", JOptionPane.ERROR_MESSAGE);
                }else{
                    int a=0;
                    for(int i = 0; i<row; i++){
                        if(list.get(value) == Integer.parseInt((String) jTable1.getValueAt(i, 8))){
                            a=i;
                            break;
                        }
                    }
                    Object[] searchedData = {
                        "Model no:", (String) jTable1.getValueAt(a, 0),
                        "Wine Name:", (String) jTable1.getValueAt(a, 1),
                        "Brand:", (String) jTable1.getValueAt(a, 2),
                        "Country:", (String) jTable1.getValueAt(a, 3),
                        "Vintage:", (String) jTable1.getValueAt(a, 4),
                        "Bottle Size:", (String) jTable1.getValueAt(a, 5),
                        "Wine Type:", (String) jTable1.getValueAt(a, 6),
                        "Alcohol:", (String) jTable1.getValueAt(a, 7),
                        "Price:", (String) jTable1.getValueAt(a, 8),
                    };
                    JOptionPane.showMessageDialog(this, searchedData, "Your Search", JOptionPane.INFORMATION_MESSAGE);                
                }
            }
        }catch(NumberFormatException | StackOverflowError e){
            JOptionPane.showMessageDialog(this, "Input Message", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField1KeyPressed

