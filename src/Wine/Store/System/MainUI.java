    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        // TODO add your handling code here:
        try{
            JTextField rowVal = new JTextField();

            Object[] message = {"Which Row to update?", rowVal};

            int option = JOptionPane.showConfirmDialog(this, message, "Update Row", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
            if(option == JOptionPane.OK_OPTION){
                int row = Integer.parseInt(rowVal.getText())-1;
                this.manageRow = row;
                String toUpdateWName = (String) jTable1.getValueAt(row, 1);
                int toUpdateVintage = Integer.parseInt((String) jTable1.getValueAt(row, 4));
                String toUpdateBrand = (String) jTable1.getValueAt(row, 2);
                String toUpdateCountry = (String) jTable1.getValueAt(row, 3);
                String toUpdateMNum = (String) jTable1.getValueAt(row, 0);
                float toUpdateAlch = Float.parseFloat((String)jTable1.getValueAt(row, 7));
                float toUpdateBtlSize = Float.parseFloat((String) jTable1.getValueAt(row, 5));
                int toUpdatePrice = Integer.parseInt((String) jTable1.getValueAt(row, 8));
                String toUpdateWType = (String) jTable1.getValueAt(row, 6);

                jTextField8.setText(toUpdateWName);
                jTextField9.setText(toUpdateBrand);
                jTextField10.setText(toUpdateCountry);
                jTextField12.setText(String.valueOf(toUpdateVintage));
                jTextField11.setText(toUpdateMNum);
                jTextField13.setText(String.valueOf(toUpdateAlch));
                jTextField15.setText(String.valueOf(toUpdatePrice));

                float cbVal1 = Float.parseFloat(jCheckBox5.getText());
                float cbVal2 = Float.parseFloat(jCheckBox6.getText());
                float cbVal3 = Float.parseFloat(jCheckBox7.getText());
                float cbVal4 = Float.parseFloat(jCheckBox8.getText());

                if(toUpdateBtlSize == cbVal1){
                    jCheckBox5.setSelected(true);
                }else if(toUpdateBtlSize == cbVal2){
                    jCheckBox6.setSelected(true);
                }else if(toUpdateBtlSize == cbVal3){
                    jCheckBox7.setSelected(true);
                }else{
                    jCheckBox8.setSelected(true);
                }

                String rbVal1 = jRadioButton4.getText();
                String rbVal2 = jRadioButton5.getText();
                String rbVal3 = jRadioButton6.getText();

                if(toUpdateWType.equals(rbVal1)){
                    jRadioButton4.setSelected(true);
                }else if(toUpdateWType.equals(rbVal2)){
                    jRadioButton5.setSelected(true);
                }else{
                    jRadioButton6.setSelected(true);
                }

                jFrame3.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Update Canceled");
            }
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this,"Invalid Input Given","Invalid Input Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jPanel2MouseClicked

